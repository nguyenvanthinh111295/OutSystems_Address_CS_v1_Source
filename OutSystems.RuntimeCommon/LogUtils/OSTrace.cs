        private static void FlushOldTimers() {
            Hashtable keysToKill = new Hashtable();
            string flushReport = "";

            lock (ongoingTimers) {
                foreach (var entry in ongoingTimers) {
                    if (DateTime.Now.Subtract(entry.Value) > TimerFlushTimeout) {
                        keysToKill.Add(entry.Key, null);
                    }
